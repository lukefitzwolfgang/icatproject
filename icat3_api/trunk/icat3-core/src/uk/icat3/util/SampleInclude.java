package uk.icat3.util;

/**
 * Set of information to return with sample.
 * Having more information returned means the query will take longer.
 *
 * @author gjd37
 */
public enum SampleInclude {
        
    ALL_SAMPLE_ID,
    NONE;
    
    public boolean isAllSampleId () {
        if (this == ALL_SAMPLE_ID)
            return true;
        return false;
    }
  
}
